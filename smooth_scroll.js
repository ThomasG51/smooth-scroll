$('.scroll_btn').click(function(){

    // On recupere la cible du bouton cliqué
    let id = $(this).attr('href');

    // On indique l'ancre de la section jusqu'a laquel il faut scroller
    let section_scope = $(id).offset().top;

    // On recupere la taille de la navbar pour position la section juste dessous
    let nav_height = $('.nav').height();

    // On crée une animation lors du scroll
    $('html,body').animate({
        scrollTop: (section_scope - nav_height)
    }, 500);
});


